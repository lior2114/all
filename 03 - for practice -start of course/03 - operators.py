a = 5

a += 3  # הוספה והקצאה: a = a + 3
a -= 2  # חיסור והקצאה: a = a - 2
a *= 4  # כפל והקצאה: a = a * 4
a /= 2  # חילוק והקצאה: a = a / 2
a %= 3  # מודולו והקצאה: a = a % 3
a //= 2  # חילוק שלם והקצאה: a = a // 2
a **= 3  # חזקה והקצאה: a = a ** 3
a &= 2  # AND בינארי והקצאה: a = a & 2
a |= 1  # OR בינארי והקצאה: a = a | 1
a ^= 3  # XOR בינארי והקצאה: a = a ^ 3
a >>= 1  # הזזה ימינה והקצאה: a = a >> 1
a <<= 2  # הזזה שמאלה והקצאה: a = a << 2