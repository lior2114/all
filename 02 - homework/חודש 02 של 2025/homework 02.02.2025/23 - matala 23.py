# קלט מספר מהמשתמש
number = input("רפסמ סנכה: ")

# הפוך את סדר הספרות
reversed_number = number[::-1] # תחביר [::] משמש לחיתוך, והפרמטר השלישי הוא הצעד (step). כאשר הצעד הוא -1, זה אומר לעבור על הרשימה או המחרוזת מהסוף להתחלה, ובכך להפוך את הסדר.

# הדפס את המספר ההפוך
print(":אוה ףופהה רפסמה", reversed_number)