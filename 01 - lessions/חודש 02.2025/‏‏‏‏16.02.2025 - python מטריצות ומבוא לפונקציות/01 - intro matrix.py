matrix = [ #זה רשימה של רשימות
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9],
    ["a", "b", "c"]
]
print(matrix)
print(matrix[0])
print(matrix[0][2]) #זה ידפיס את המספר 3 שהוא נמצא ברשימה הראשונה במיקום ה 2 (כי המיקום מתחיל מ 0)
print(matrix[1][1])#זה ידפיס את המספר 5 שהוא נמצא ברשימה השנייה במיקום ה 1 (כי המיקום מתחיל מ 0)
frinds = matrix[3] #זה יקח את הרשימה שנמצאת במקום ה 3 ברשימה הראשית ויכניס אותה למשתנה חדש
print(frinds)