matrix = [ # זה רשימה של רשימות/אוסף של רשימות
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]

max = matrix[0][0] #הגדרת המקסימום כהתחלה כאיבר הראשון במטריצה
for numbers in matrix:
    for num in numbers:#לולאה שעוברת על כל המספרים במטריצה כל מספר בתוך הרשימה הראשונה ואז השניה ואז השלישית
        if num > max: #אם המספר שהוא עובר כרגע גדול מהמקסימום
            max = num #המקסימום הופך להיות המספר שהוא עובר כרגע
print(max) #הדפסת המקסימום

