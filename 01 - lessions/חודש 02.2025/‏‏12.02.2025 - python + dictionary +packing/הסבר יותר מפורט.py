print("============= סט - רשימה ללא כפילויות =====================")
numbers = {10, 20, 20, 10}
print(numbers)  # מדפיס סט ללא כפילויות
# פלט: {10, 20}

# הסבר:
# סט (Set) הוא מבנה נתונים שמכיל ערכים ייחודיים בלבד, כלומר, אין כפילויות.
# כאשר יוצרים סט עם ערכים כפולים, הסט ישמור רק ערך אחד מכל סוג.

print("============= מילון - dictionary =====================")
products = {"apple": 3.5, "Banana": 4, "peach": 4}
print(products)
# פלט: {'apple': 3.5, 'Banana': 4, 'peach': 4}

# הסבר:
# מילון (Dictionary) הוא מבנה נתונים שמכיל זוגות של מפתח וערך.
# כל מפתח במילון חייב להיות ייחודי, ואם יש מפתח כפול, הערך האחרון ידרוס את הקודם.

print(products["apple"])  # מדפיס ערך לפי מפתח
# פלט: 3.5

# הסבר:
# ניתן לגשת לערך במילון באמצעות המפתח שלו.

products["Banana"] = 200
print(products)  # מדפיס מילון עם ערך מעודכן
# פלט: {'apple': 3.5, 'Banana': 200, 'peach': 4}

# הסבר:
# ניתן לעדכן ערך במילון על ידי שימוש במפתח שלו והקצאת ערך חדש.

for key in products:
    print(key, end="|")  # מדפיס מפתחות
# פלט: apple|Banana|peach|

# הסבר:
# ניתן לעבור על כל המפתחות במילון באמצעות לולאת for.

for key in products.keys():
    print(key, end="|")  # מדפיס מפתחות
# פלט: apple|Banana|peach|

# הסבר:
# ניתן להשתמש במתודה keys() כדי לקבל את כל המפתחות במילון.

for value in products.values():
    print(value, end="|")  # מדפיס ערכים
# פלט: 3.5|200|4|

# הסבר:
# ניתן להשתמש במתודה values() כדי לקבל את כל הערכים במילון.

for key, value in products.items():
    print(f"{key} ===> {value}")  # מדפיס מפתחות וערכים
# פלט:
# apple ===> 3.5
# Banana ===> 200
# peach ===> 4

# הסבר:
# ניתן להשתמש במתודה items() כדי לקבל את כל זוגות המפתח-ערך במילון.

# דוגמאות נוספות:
print("============= הוספת פריט למילון =====================")
products["orange"] = 6
print(products)  # מדפיס מילון עם הפריט החדש
# פלט: {'apple': 3.5, 'Banana': 200, 'peach': 4, 'orange': 6}

# הסבר:
# ניתן להוסיף פריט חדש למילון על ידי הקצאת מפתח חדש וערך.

print("============= מחיקת פריט מהמילון =====================")
del products["apple"]
print(products)  # מדפיס מילון ללא הפריט שנמחק
# פלט: {'Banana': 200, 'peach': 4, 'orange': 6}

# הסבר:
# ניתן למחוק פריט מהמילון באמצעות הפקודה del והמפתח של הפריט.

print("============= בדיקת קיום מפתח במילון =====================")
print("Banana" in products)  # מדפיס True אם המפתח קיים
# פלט: True

# הסבר:
# ניתן לבדוק אם מפתח קיים במילון באמצעות האופרטור in.

print("============= איחוד שני סטים =====================")
set1 = {1, 2, 3}
set2 = {3, 4, 5}
union_set = set1 | set2
print(union_set)  # מדפיס את האיחוד של שני הסטים
# פלט: {1, 2, 3, 4, 5}

# הסבר:
# ניתן לבצע איחוד (Union) של שני סטים באמצעות האופרטור |.
# האיחוד מכיל את כל הערכים מכל אחד מהסטים, ללא כפילויות.

print("============= חיתוך שני סטים =====================")
intersection_set = set1 & set2
print(intersection_set)  # מדפיס את החיתוך של שני הסטים
# פלט: {3}

# הסבר:
# ניתן לבצע חיתוך (Intersection) של שני סטים באמצעות האופרטור &.
# החיתוך מכיל רק את הערכים שנמצאים בשני הסטים.